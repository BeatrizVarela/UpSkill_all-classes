"""
Geometry calculations
"""
    
def squarePerimeter(d):
    return 4*d

def squareArea(x):
    return x*x

def retangulArea(h,b):
    return h*b
    
def circlePerimeter (y):
    return 2 * 3.14 * y

def circleArea(z):
    return 3.14 * z * z

def circleVolume (a):
    return 4.0/3.0 * 3.14 * a**3
